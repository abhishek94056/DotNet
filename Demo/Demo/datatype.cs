using System;
using System.Collections.Generic;
using System.Text;

namespace Demo
{
    internal class datatype
    {
        internal void pd()
        {
            int a = 1;
            byte b = 2;
            short s = 3;
            double d = 4;
            float f = 5.00F;
            bool bo = true;
            char c = 'a';
            string str = "Abhishek";
            Console.WriteLine("a={0},b={1},s={2},d={3},f={4},bo={5},c={6},str={7}", a,b,s,d,f,bo,c,str);
        }

        internal void dd()
        {
            Int16 a = 1;
            Byte b = 2;
            Double d = 3;
            Boolean bo = true;
            Char c = 'a';
            String s = "hello";
            Console.WriteLine("a={0},b={1},d={2}, bo ={3},c={5}, s={4})", a, b, d,bo,c, s);
        }
    }
}
